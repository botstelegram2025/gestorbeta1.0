app.get('/qr', async (req, res) => {
    try {
        const sessionId = req.query.sessionId || defaultSessionId;
        
        // Inicializar sess√£o se n√£o existir
        if (!sessions.has(sessionId)) {
            await connectToWhatsApp(sessionId);
            // Aguardar um pouco para QR ser gerado
            await new Promise(resolve => setTimeout(resolve, 2000));
        }
        
        const session = sessions.get(sessionId);
        
        if (!session || !session.qrCode) {
            return res.status(404).json({ 
                success: false, 
                error: `QR Code n√£o dispon√≠vel para sess√£o ${sessionId}. Tente reconectar.`,
                session_id: sessionId
            });
        }

        // Gerar imagem QR Code
        const qrImage = await QRCode.toDataURL(session.qrCode);
        
        res.json({
            success: true,
            qr: session.qrCode,
            qr_image: qrImage,
            instructions: 'Abra WhatsApp ‚Üí Configura√ß√µes ‚Üí Aparelhos conectados ‚Üí Conectar um aparelho',
            session_id: sessionId
        });
        
    } catch (error) {
        console.error('‚ùå Erro ao gerar QR:', error);
        res.status(500).json({ 
            success: false, 
            error: 'Erro ao gerar QR Code',
            session_id: req.query.sessionId || defaultSessionId
        });
    }
});

