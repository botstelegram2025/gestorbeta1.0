// Pr√©-carrega sess√µes existentes do disco (opcional)
async function preloadSessions() {
    try {
        const dirs = fs.readdirSync(SESSIONS_DIR, { withFileTypes: true })
            .filter(d => d.isDirectory())
            .map(d => d.name);
        for (const sid of dirs) {
            if (!sessions.has(sid)) {
                console.log(`üîÅ Restaurando sess√£o persistida: ${sid}`);
                try {
                    await connectToWhatsApp(sid);
                } catch (e) {
                    console.error(`Falha ao restaurar sess√£o ${sid}:`, e?.message || e);
                }
            }
        }
    } catch (e) {
        console.warn("N√£o foi poss√≠vel listar SESSIONS_DIR para preload:", e?.message || e);
    }
}

