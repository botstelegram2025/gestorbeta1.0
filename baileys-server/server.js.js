// Estado global para m√∫ltiplas sess√µes
const sessions = new Map(); // sessionId -> { sock, qrCode, isConnected, status, backupInterval }
const SESSIONS_DIR = process.env.SESSIONS_DIR || path.join(__dirname, 'sessions');
if (!fs.existsSync(SESSIONS_DIR)) { fs.mkdirSync(SESSIONS_DIR, { recursive: true }); }

let defaultSessionId = 'default';

// Sistema de backup da sess√£o para PostgreSQL (por sess√£o espec√≠fica)
const saveSessionToDatabase = async (sessionId) => {
    try {
        const authPath = `./auth_info_${sessionId}`;
        if (!fs.existsSync(authPath)) return;

        const files = fs.readdirSync(authPath);
        const sessionData = {};
        
        for (const file of files) {
            if (file.endsWith('.json')) {
                const filePath = path.join(authPath, file);
                const content = fs.readFileSync(filePath, 'utf8');
                sessionData[file] = content;
            }
        }

        // Salvar no banco via API Python com ID da sess√£o
        if (Object.keys(sessionData).length > 0) {
            await fetch('http://localhost:5000/api/session/backup', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({ 
                    session_data: sessionData,
                    session_id: sessionId
                })
            });
            console.log(`üíæ Sess√£o ${sessionId} salva no banco de dados`);
        }
    } catch (error) {
        console.log(`‚ö†Ô∏è Erro ao salvar sess√£o ${sessionId}:`, error.message);
    }
};

// Restaurar sess√£o do banco de dados (por sess√£o espec√≠fica)
const restoreSessionFromDatabase = async (sessionId) => {
    try {
        const response = await fetch(`http://localhost:5000/api/session/restore?session_id=${sessionId}`);
        if (response.ok) {
            const { session_data } = await response.json();
            
            if (session_data && Object.keys(session_data).length > 0) {
                const authPath = `./auth_info_${sessionId}`;
                if (!fs.existsSync(authPath)) {
                    fs.mkdirSync(authPath, { recursive: true });
                }

                for (const [filename, content] of Object.entries(session_data)) {
                    const filePath = path.join(authPath, filename);
                    fs.writeFileSync(filePath, content);
                }
                
                console.log(`üîÑ Sess√£o ${sessionId} restaurada do banco de dados`);
                return true;
            }
        }
    } catch (error) {
        console.log(`‚ö†Ô∏è Erro ao restaurar sess√£o ${sessionId}:`, error.message);
    }
    return false;
};

// Fun√ß√£o para conectar ao WhatsApp (por sess√£o espec√≠fica)
async function connectToWhatsApp(sessionId = defaultSessionId) {
const authPath = path.join(SESSIONS_DIR, String(sessionId));
const { state, saveCreds } = await useMultiFileAuthState(authPath);

    try {
        console.log(`üîÑ Iniciando conex√£o com WhatsApp para sess√£o ${sessionId}...`);
        
        // Criar ou obter dados da sess√£o
        if (!sessions.has(sessionId)) {
            sessions.set(sessionId, {
                sock: null,
                qrCode: '',
                isConnected: false,
                status: 'disconnected',
                backupInterval: null
            });
        }
        
        const session = sessions.get(sessionId);
        
        // Primeiro tentar restaurar sess√£o do banco
        await restoreSessionFromDatabase(sessionId);
        
        const { state, saveCreds } = await useMultiFileAuthState(`./auth_info_${sessionId}`);
        
        session.sock = makeWASocket({ auth: state, printQRInTerminal: false, 
            auth: state,
            printQRInTerminal: false
        });

        // Salvar credenciais e backup autom√°tico
        session.sock.ev.on('creds.update', async () => {
            await saveCreds();
            // Fazer backup imediato a cada update das credenciais
            setTimeout(() => saveSessionToDatabase(sessionId), 1000);
        });
        
        // Monitorar conex√£o
        session.sock.ev.on('connection.update', (update) => {
            const { connection, lastDisconnect, qr } = update;
            
            if (connection !== session.status) {
                console.log(`üì± Sess√£o ${sessionId} - Status:`, connection);
            }
            
            if (qr) {
                console.log(`üì± QR Code gerado para sess√£o ${sessionId}!`);
                session.qrCode = qr;
                session.status = 'qr_ready';
            }
            
            if (connection === 'close') {
                session.isConnected = false;
                session.status = 'disconnected';
                
                const shouldReconnect = (lastDisconnect?.error)?.output?.statusCode !== DisconnectReason.loggedOut;
                console.log(`üîå Sess√£o ${sessionId} - Conex√£o fechada. Reconectar?`, shouldReconnect);
                
                // Tratamento de reconex√£o espec√≠fico por sess√£o
                if ((lastDisconnect?.error)?.output?.statusCode === DisconnectReason.badSession ||
                    (lastDisconnect?.error)?.output?.statusCode === DisconnectReason.restartRequired ||
                    lastDisconnect?.error?.message?.includes('device_removed') ||
                    lastDisconnect?.error?.message?.includes('conflict')) {
                    console.log(`üßπ Sess√£o ${sessionId} - Aguardando devido a conflito...`);
                    session.qrCode = '';
                    session.status = 'disconnected';
                    setTimeout(() => connectToWhatsApp(sessionId), 30000);
                } else if (shouldReconnect) {
                    setTimeout(() => connectToWhatsApp(sessionId), 10000);
                }
            } else if (connection === 'open') {
                session.isConnected = true;
                session.status = 'connected';
                session.qrCode = '';
                console.log(`‚úÖ Sess√£o ${sessionId} - WhatsApp conectado!`);
                
                // Configurar backup autom√°tico
                if (session.backupInterval) clearInterval(session.backupInterval);
                session.backupInterval = setInterval(() => saveSessionToDatabase(sessionId), 2 * 60 * 1000);
                
                // Fazer backup imediato ap√≥s conectar
                setTimeout(() => saveSessionToDatabase(sessionId), 5000);
                console.log(`üìû Sess√£o ${sessionId} - N√∫mero:`, session.sock.user.id);
            } else if (connection === 'connecting') {
                if (session.status !== 'connecting') {
                    session.status = 'connecting';
                    console.log(`üîÑ Sess√£o ${sessionId} - Conectando...`);
                }
            } else if (connection === 'close') {
                const shouldReconnect = (lastDisconnect?.error)?.output?.statusCode !== DisconnectReason.loggedOut;
                session.isConnected = false;
                session.status = 'close';
                session.qrCode = '';
                
                console.log(`üîå Sess√£o ${sessionId} - Conex√£o fechada. Reconectar? ${shouldReconnect}`);
                
                if (shouldReconnect) {
                    // Aguardar 3 segundos antes de reconectar
                    setTimeout(() => {
                        console.log(`üîÑ Reconectando sess√£o ${sessionId} automaticamente...`);
                        connectToWhatsApp(sessionId);
                    }, 3000);
                }
            }
        });

    } catch (error) {
        console.error(`‚ùå Erro ao conectar sess√£o ${sessionId}:`, error);
        const session = sessions.get(sessionId);
        if (session) {
            session.status = 'error';
        }
    }
}

