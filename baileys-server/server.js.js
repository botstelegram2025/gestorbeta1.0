// Enviar mensagem (com suporte a sess√µes espec√≠ficas)
app.post('/send-message', async (req, res) => {
    try {
        const { number, message, session_id } = req.body;
        const sessionId = session_id || defaultSessionId;
        
        if (!number || !message) {
            return res.status(400).json({
                success: false,
                error: 'N√∫mero e mensagem s√£o obrigat√≥rios'
            });
        }
        
        const session = sessions.get(sessionId);
        
        if (!session || !session.isConnected) {
            return res.status(400).json({
                success: false,
                error: `WhatsApp n√£o conectado para sess√£o ${sessionId}`,
                session_id: sessionId
            });
        }
        
        // Formatar n√∫mero
        const jid = number.includes('@') ? number : `${number}@s.whatsapp.net`;
        
        // Enviar mensagem
        const result = await session.sock.sendMessage(jid, { text: message });
        
        console.log(`‚úÖ Mensagem enviada via sess√£o ${sessionId}:`, number, message.substring(0, 50) + '...');
        
        res.json({
            success: true,
            messageId: result.key.id,
            timestamp: new Date().toISOString(),
            session_id: sessionId
        });
        
    } catch (error) {
        console.error(`‚ùå Erro ao enviar mensagem via sess√£o ${sessionId}:`, error);
        res.status(500).json({
            success: false,
            error: error.message,
            session_id: sessionId
        });
    }
});

// Reconectar (com suporte a sess√µes espec√≠ficas)
app.post('/reconnect/:sessionId', async (req, res) => {
    try {
        const sessionId = req.params.sessionId || defaultSessionId;
        console.log(`üîÑ Reconectando sess√£o ${sessionId}...`);
        
        // Limpar sess√£o existente
        if (sessions.has(sessionId)) {
            const session = sessions.get(sessionId);
            if (session.sock) {
                session.sock.end();
            }
            if (session.backupInterval) {
                clearInterval(session.backupInterval);
            }
            sessions.delete(sessionId);
        }
        
        // Iniciar nova conex√£o
        setTimeout(() => connectToWhatsApp(sessionId), 1000);
        
        res.json({
            success: true,
            message: `Reconex√£o iniciada para sess√£o ${sessionId}`,
            session_id: sessionId
        });
        
    } catch (error) {
        console.error(`‚ùå Erro ao reconectar sess√£o ${sessionId}:`, error);
        res.status(500).json({
            success: false,
            error: error.message,
            session_id: sessionId
        });
    }
});

